            <h1>Python API</h1>
          </div>
	The Python module allows you to connect to the OpenML server from Python programs. This means that you can download and upload OpenML dataset, tasks, run Python algorithms on them, and share the results. All in a few lines on Python.

  <h2 id="download">Download</h2>
	The Python module is currently under active development on <a href="https://github.com/openml/python">GitHub</a>.

  <h2 id="download">Quickstart</h2>
  <a href="http://openml.readthedocs.org/en/latest/">Check out the documentation</a> to get started. There is also a <a href="https://github.com/openml/python/blob/develop/examples/OpenMLDemo.ipynb">Jupyter Notebook</a> with examples.

	<h2 id="issues">Issues</h2>
	Having questions? Did you run into an issue? Let us know via the <a href="https://github.com/openml/python/issues"> OpenML Python issue tracker</a>.
